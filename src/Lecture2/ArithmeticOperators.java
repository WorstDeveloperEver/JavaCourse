package Lecture2;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int xInt = 17, yInt = 6;
        double xDouble = 17, yDouble = 6;
        System.out.println("+ Integer = " + (xInt + yInt) + " Double = " + (xDouble + yDouble));
        System.out.println("- Integer = " + (xInt - yInt) + " Double = " + (xDouble - yDouble));
        System.out.println("/ Integer = " + (xInt / yInt) + " Double = " + (xDouble / yDouble));
        System.out.println("% Integer = " + (xInt % yInt) + " Double = " + (xDouble % yDouble));
    }
}
